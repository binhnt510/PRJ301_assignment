package entity.accesscontrol;

/**
 *
 * @author admin
 */
public class Permission {
    private String name;
    private String url;

    public Permission(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
}
